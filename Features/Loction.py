import requests
import speech_recognition as sr
import webbrowser as web
import pyttsx3
import bs4
engine = pyttsx3.init('sapi5')
voices = engine.getProperty('voices')
engine.setProperty('voices',voices[0].id)

def Speak(audio):
    print(" ")
    print(f": {audio}")
    engine.say(audio)
    engine.runAndWait()
    print(" ")

def My_Location(Data):
    ip_add = requests.get('https://api.ipify.org').text

    url = 'https://get.geojs.io/v1/ip/geo/' + ip_add + '.json'

    geo_q = requests.get(url)

    geo_d = geo_q.json()

    state = geo_d['city']

    country = geo_d['country']

    Speak(f"Sir, You are now in {state}, {country}.")



def CoronaVirus(Country):

    countries = str(Country).replace(" ","")

    url = f"https://www.worldometers.info/coronavirus/country/{countries}/"

    result = requests.get(url)

    soups = bs4.BeautifulSoup(result.text,'lxml')

    corona = soups.find_all('div',class_ = 'maincounter-number')

    Data = []

    for case in corona:

        span = case.find('span')

        Data.append(span.string)

    if len(Data) == 3:
        cases , Death , recovored = Data
        Speak(f"Cases : {cases}")
        Speak(f"Deaths : {Death}")
        Speak(f"Recovered : {recovored}")
    else:
        Speak("Sorry, I was unable to fetch the corona cases information for the given country.")

